package Tugas;

public class IndexCheck {
	int Data[];
	int Array;
	 
	public IndexCheck(int[] insert, int check) {
			this.Data = insert;
			this.Array = check;
		}

	public int[] getData() {
		return Data;
	}
	public void setData(int[] data) {
		Data = data;
	}

	public int getArray() {
		return Array;
	}
	public void setArray(int array) {
		Array = array;
	}
	 
	 
}
